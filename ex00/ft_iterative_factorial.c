# include <stdio.h>

int ft_iterative_factorial(int nb)

{
	int i;
	int fact;
	int num =nb;

	fact =1;
	if (num <=0)
		fact = 1;
	else
		(i = 1);
		 while(i <= num)
		 {
		 fact = fact + 1;
		 i = i + 1;
		 }
	return fact;
}

